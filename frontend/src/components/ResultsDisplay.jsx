import React from 'react';

const SEVERITY_MAP = {
    'Critical': { color: '#b91c1c', icon: '🔴', level: 3 },
    'High Risk': { color: '#c2410c', icon: '🟠', level: 2 },
    'Warning': { color: '#b45309', icon: '⚠️', level: 1 },
    'Notice': { color: '#1d4ed8', icon: '🔵', level: 0 },
};

function getSeverity(factor) {
    for (const [key, val] of Object.entries(SEVERITY_MAP)) {
        if (factor.startsWith(key)) return { ...val, label: key };
    }
    return { color: '#15803d', icon: '✅', level: -1, label: 'Satisfactory' };
}

function RiskFactorCard({ factor }) {
    const sev = getSeverity(factor);
    const detail = factor.replace(/^(Critical|High Risk|Warning|Notice):\s*/i, '');
    return (
        <div className="risk-factor-card" style={{ borderLeftColor: sev.color }}>
            <div className="risk-factor-icon">{sev.icon}</div>
            <div className="risk-factor-body">
                <span className="risk-factor-tag" style={{ backgroundColor: `${sev.color}15`, color: sev.color }}>
                    {sev.label}
                </span>
                <p className="risk-factor-text">{detail}</p>
            </div>
        </div>
    );
}

function ResultsDisplay({ result }) {
    const { probability, label, factors } = result;
    const isHighRisk = probability > 50;
    const gaugeColor = isHighRisk ? 'var(--red)' : 'var(--green)';
    const conicGradient = `conic-gradient(${gaugeColor} ${probability}%, #e5e7eb 0)`;

    const sortedFactors = [...factors].sort((a, b) => getSeverity(b).level - getSeverity(a).level);

    return (
        <div className="card results-container slide-up">
            {/* Report Header */}
            <div className="card-header">
                <div className="card-header-icon" style={{ background: isHighRisk ? 'var(--red)' : 'var(--green)' }}>
                    {isHighRisk ? '⚠' : '✓'}
                </div>
                <h3>Credit Assessment Report</h3>
            </div>

            {/* Gauge */}
            <div className="gauge-section">
                <div className="gauge-wrapper" style={{ background: conicGradient }}>
                    <div className="gauge-inner">
                        <span className="gauge-value">{probability.toFixed(1)}%</span>
                        <span className="gauge-label">Default Risk</span>
                    </div>
                </div>
                <div className={`prediction-label ${isHighRisk ? 'danger' : 'success'} mt-3`}>
                    {isHighRisk ? '⚠ ' : '✔ '}{label.toUpperCase()}
                </div>
                <p className="verdict-sub mt-2">
                    {isHighRisk
                        ? 'This applicant exhibits elevated default risk. Review the identified factors carefully before proceeding.'
                        : 'This applicant falls within acceptable risk parameters and meets standard lending criteria.'}
                </p>
            </div>

            {/* Risk Factors */}
            <div className="border-t pt-4">
                <h4>Risk Factors Identified</h4>
                <p style={{ color: 'var(--text-muted)', fontSize: '0.78rem', marginTop: '0.3rem', marginBottom: '0.75rem' }}>
                    {sortedFactors.length} factor{sortedFactors.length !== 1 ? 's' : ''} detected — sorted by severity
                </p>
                <div className="risk-factors-list">
                    {sortedFactors.map((factor, idx) => (
                        <RiskFactorCard key={idx} factor={factor} />
                    ))}
                </div>
            </div>

            {/* Disclaimer */}
            <div style={{ marginTop: '1.25rem', padding: '0.65rem 0.85rem', background: '#fffbeb', border: '1px solid #fde68a', borderRadius: '4px', fontSize: '0.72rem', color: '#92400e', lineHeight: 1.6 }}>
                <strong>Disclaimer:</strong> This report is generated by an AI model and is intended for informational purposes only. 
                Final lending decisions must be made by authorized bank officers in accordance with RBI guidelines.
            </div>
        </div>
    );
}

export default ResultsDisplay;
